    public static int prob1(String input) {
        Map<String, Integer> registers = new HashMap<>();
        for (String line : input.split("\\r?\\n")) {
            String[] op = line.split("if");
            String[] right = op[1].trim().split(" ");
            String a = right[0];
            String comp = right[1];
            int b = Integer.parseInt(right[2]);

            String[] left = op[0].trim().split(" ");
            String reg = left[0];
            String inc = left[1];
            int val = Integer.parseInt(left[2]);

            operation(registers, reg,
                    inc, val, registers.getOrDefault(a, 0),
                    comp, b);
        }
        return findMax(registers);
    }

    public static int operation(Map<String, Integer> registers, String reg, String inc, int val,
                                int a, String comp, int b) {
        Boolean res;
        switch (comp) {
            case "<":
                res = a < b;
                break;
            case ">":
                res = a > b;
                break;
            case ">=":
                res = a >= b;
                break;
            case "<=":
                res = a <= b;
                break;
            case "!=":
                res = a != b;
                break;
            case "==":
                res = a == b;
                break;
            default:
                 throw new UnsupportedOperationException("Nuh uh");
        }

        if (res) {
            int registerVal = registers.getOrDefault(reg, 0);
            if (inc.equals("inc")) {
                registerVal += val;
            } else if (inc.equals("dec")) {
                registerVal -= val;
            }
            registers.put(reg, registerVal);
        }
        return findMax(registers);
    }

    public static int findMax(Map<String, Integer> registers) {
        int max = Integer.MIN_VALUE;
        for (String i : registers.keySet()) {
            if (registers.get(i) > max) {
                max = registers.get(i);
            }
        }
        return max;
    }

    public static int prob2(String input) {
        int max = Integer.MIN_VALUE;
        Map<String, Integer> registers = new HashMap<>();
        for (String line : input.split("\\r?\\n")) {
            String[] op = line.split("if");
            String[] right = op[1].trim().split(" ");
            String a = right[0];
            String comp = right[1];
            int b = Integer.parseInt(right[2]);

            String[] left = op[0].trim().split(" ");
            String reg = left[0];
            String inc = left[1];
            int val = Integer.parseInt(left[2]);

            int curMax = operation(registers, reg,
                    inc, val, registers.getOrDefault(a, 0),
                    comp, b);
            if (curMax > max) {
                max = curMax;
            }
        }
        return max;
    }
